
public class NewLinkedList {
	int val;
	String name;
	String address;
	
	NewLinkedList() {
		this.val     = 0;
		this.name    = "";
		this.address = "";
	}
	
	NewLinkedList(int age, String name, String address) {
		this.val = age;
		this.name = name;
		this.address = address;
	}
}
