package vocab;

public class Vocab {
    private String kor;
    private String eng;


    public void setKor(String kor) {
        this.kor = kor;
    }

    public void setEng(String eng) {
        this.eng = eng;
    }

    public String getKor() {
        return this.kor;
    }

    public String getEng() {
        return this.eng;
    }

}
